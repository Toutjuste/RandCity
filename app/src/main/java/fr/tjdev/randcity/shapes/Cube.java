    /**
     * The generated array will contain vertices of faces in this order :
     * - front
     * - right
     * - back
     * - left
     * - top
     * - bottom
     *
     * You must specify the width, height, and depth of the cube and the top left corner of the bottom face
     * This point (the view is from the top to the bottom)
     * \/
     * ------
     * |    |
     * |    |
     * ------
     *
     * You can also specify that you don't want to create the bottom face
     */
    static public float[] generateCuboid(float topLeftX, float topLeftY, float topLeftZ,
                                         float width, float height, float depth, boolean generateBottom) {
        // 3 vertices per triangle * 2 triangles per face * 6 faces
        float[] cube = new float[IShape.VERTEX_DATA_ELEMENTS * 3 * 2 * 6];

        // At first, define all different vertices (8 different)
        final float[] vertices = {
                topLeftX, topLeftY + height, topLeftZ + depth,
                topLeftX, topLeftY, topLeftZ + depth,
                topLeftX + width, topLeftY, topLeftZ + depth,
                topLeftX + width, topLeftY + height, topLeftZ + depth,
                topLeftX + width, topLeftY + height, topLeftZ,
                topLeftX + width, topLeftY, topLeftZ,
                topLeftX, topLeftY, topLeftZ,
                topLeftX, topLeftY + height, topLeftZ
        };

        // Define the order of vertices
        // This order is the same for the normals and texture coordinates arrays for the Cube.
        final short[] order = {
                0, 1, 3, 1, 2, 3, // front
                3, 2, 4, 2, 5, 4, // right
                4, 5, 7, 5, 6, 7, // back
                7, 6, 0, 6, 1, 0, // left
                7, 0, 4, 0, 3, 4, // top
                5, 2, 6, 2, 1, 6  // bottom
        };

        // If we don't render the bottom, we skip the last 6 index in order list
        int finalLength = order.length;
        if (!generateBottom) {
            finalLength -= 6;
        }

        // Now we can map these vertices into the final array
        int indexCube = 0;
        for (int i = 0; i < finalLength; ++i) {
            cube[indexCube] = vertices[order[i] * 3];
            cube[indexCube + 1] = vertices[order[i] * 3 + 1];
            cube[indexCube + 2] = vertices[order[i] * 3 + 2];
            indexCube += 3;
        }

        return cube;
    }
